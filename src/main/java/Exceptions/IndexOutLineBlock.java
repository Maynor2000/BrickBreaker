package Exceptions;

/**
 *
 * @author mayk
 */
public class IndexOutLineBlock extends Exception{

    public IndexOutLineBlock() {
    }

    public IndexOutLineBlock(String message) {
        super(message);
    }

    public IndexOutLineBlock(String message, Throwable cause) {
        super(message, cause);
    }

    public IndexOutLineBlock(Throwable cause) {
        super(cause);
    }

    public IndexOutLineBlock(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
    
}
